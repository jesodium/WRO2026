let LANG = (() => {
  const saved = localStorage.getItem("lang");
  return LANGS.some(l => l.code === saved) ? saved : "en";
})();

export const getLang = () => LANG;
export function setLang(code) {
  if (LANGS.some(l => l.code === code)) { LANG = code; localStorage.setItem("lang", code); }
  return LANG;
}
const meta = () => LANGS.find(l => l.code === LANG) || LANGS[0];
export const ttsVoice = () => meta().voice;
export const speechLang = () => meta().speech;

export function t(key, params) {
  let s = DICT[LANG]?.[key] ?? DICT.en[key] ?? key;
  if (params) s = s.replace(/{(\w+)}/g, (_, k) => (params[k] ?? `{${k}}`));
  return s;
}
